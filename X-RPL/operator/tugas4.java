public class tugas4 {
    public static void main(String[] args) {
        // Nilai seluruh Siswa
        int nilaiSiswa1 = 80;
        int nilaiSiswa2 = 95;
        int nilaiSiswa3 = 75;

        System.out.println("-----------------------");
        System.out.println("Nilai Siswa 1 = " + nilaiSiswa1);
        System.out.println("Nilai Siswa 2 = " + nilaiSiswa2);
        System.out.println("Nilai Siswa 3 = " + nilaiSiswa3);

        double var = 80;
        var += 95;
        var += 75;
        System.out.println("-----------------------");
        System.out.println("Total Nilai Siswa = " + var);

        var /= 3;
        System.out.println("-----------------------");
        System.out.println("Nilai Rata - Rata = " + var);
    }
}
